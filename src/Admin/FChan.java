    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Connection connection = DBConnection.getConnection();

        int selectedRow = tblQuanLy.getSelectedRow();
        
        if (selectedRow != -1) {
            String TAIKHOAN = (String) tblQuanLy.getValueAt(selectedRow, 0);
            boolean XACNHAN = (boolean) tblQuanLy.getValueAt(selectedRow, 1);
            
            String updateSQL = "UPDATE TAIKHOAN SET XACNHAN = ? WHERE TAIKHOAN = ?";

            try (PreparedStatement preparedStatement = connection.prepareStatement(updateSQL)) {
                preparedStatement.setBoolean(1, XACNHAN);
                preparedStatement.setString(2, TAIKHOAN);
              
                int rowsUpdated = preparedStatement.executeUpdate();

                if (rowsUpdated > 0) {
                    FillMH(connection);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FServer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
           
        DBConnection.closeConnection(connection);
    }//GEN-LAST:event_jButton2ActionPerformed

