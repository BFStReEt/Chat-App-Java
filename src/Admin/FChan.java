    public void FillMH(Connection connection){
        DefaultTableModel modelQuanLy = (DefaultTableModel) tblQuanLy.getModel();
        DefaultTableModel modelQuanLy1 = (DefaultTableModel) tblQuanLy1.getModel();
    
        modelQuanLy.setRowCount(0); 
        modelQuanLy1.setRowCount(0); 
    
        String sql = "SELECT * FROM TAIKHOAN";

        try (PreparedStatement preparedStatement = connection.prepareStatement(sql);
             ResultSet resultSet = preparedStatement.executeQuery()) {
            while (resultSet.next()) {
                String TAIKHOAN = resultSet.getString("TAIKHOAN");
                boolean XACNHAN = resultSet.getBoolean("XACNHAN");

                Object[] rowData = {
                    TAIKHOAN,
                    XACNHAN,
                };
                if(!XACNHAN){
                    modelQuanLy1.addRow(rowData);
                }
                else{
                    modelQuanLy.addRow(rowData);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(FServer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

